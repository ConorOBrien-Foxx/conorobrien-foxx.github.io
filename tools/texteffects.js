registerApps(".text-effects-app", app => {
    const textInput = app.querySelector(".text-input");
    const textOutputs = app.querySelector(".text-outputs");
    const syncOutput = () => {
        let input = textInput.value;
        resizeTextareaToContent(textInput);
        TEXT_EFFECTS.forEach(effect => {
            if(!effect.cachedOutputElement) {
                let output = document.createElement("textarea");
                let heading = document.createElement("h3");
                heading.textContent = `${effect.name} (${effect.generate(effect.name)})`;
                effect.cachedOutputElement = output;
                textOutputs.appendChild(heading);
                textOutputs.appendChild(output);
            }
            effect.cachedOutputElement.value = effect.generate(input);
            resizeTextareaToContent(effect.cachedOutputElement);
        });
    };
    
    textInput.addEventListener("input", syncOutput);
    syncOutput();
});
